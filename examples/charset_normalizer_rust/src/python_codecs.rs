pub(crate) fn decode_charmap_strict(name: &str, payload: &[u8]) -> Option<String> {
    let table = table(name)?;
    let mut decoded = String::with_capacity(payload.len());
    for &byte in payload {
        let value = table[byte as usize];
        if value == INVALID {
            return None;
        }
        decoded.push(char::from_u32(value)?);
    }
    Some(decoded)
}

pub(crate) fn encode_charmap_strict(name: &str, text: &str) -> Option<Vec<u8>> {
    let table = table(name)?;
    let mut encoded = Vec::with_capacity(text.len());
    for ch in text.chars() {
        let value = ch as u32;
        let Some(byte) = table.iter().position(|&candidate| candidate == value) else {
            return None;
        };
        encoded.push(byte as u8);
    }
    Some(encoded)
}

pub(crate) fn decode_hz_strict(payload: &[u8]) -> Option<String> {
    encoding::all::HZ.decode(payload, DecoderTrap::Strict).ok()
}

pub(crate) fn encode_hz_strict(text: &str) -> Option<Vec<u8>> {
    let mut encoded = encoding::all::HZ.encode(text, EncoderTrap::Strict).ok()?;
    if hz_is_shifted_open(&encoded) {
        encoded.extend_from_slice(b"~}");
    }
    Some(encoded)
}

fn hz_is_shifted_open(encoded: &[u8]) -> bool {
    let mut shifted = false;
    let mut index = 0usize;
    while index + 1 < encoded.len() {
        match &encoded[index..index + 2] {
            b"~{" => {
                shifted = true;
                index += 2;
            }
            b"~}" => {
                shifted = false;
                index += 2;
            }
            b"~~" => {
                index += 2;
            }
            _ => {
                index += 1;
            }
        }
    }
    shifted
}

pub(crate) fn decode_utf32_strict(
    name: &str,
    payload: &[u8],
    sig_payload: &[u8],
) -> Option<String> {
    let (little_endian, payload) = match name {
        "utf_32" => match sig_payload {
            [0xff, 0xfe, 0x00, 0x00] => (true, payload.get(4..)?),
            [0x00, 0x00, 0xfe, 0xff] => (false, payload.get(4..)?),
            _ => return None,
        },
        "utf_32_le" => (true, payload),
        "utf_32_be" => (false, payload),
        _ => return None,
    };

    if payload.len() % 4 != 0 {
        return None;
    }

    let mut decoded = String::with_capacity(payload.len() / 4);
    for chunk in payload.chunks_exact(4) {
        let value = if little_endian {
            u32::from_le_bytes([chunk[0], chunk[1], chunk[2], chunk[3]])
        } else {
            u32::from_be_bytes([chunk[0], chunk[1], chunk[2], chunk[3]])
        };
        decoded.push(char::from_u32(value)?);
    }
    Some(decoded)
}

pub(crate) fn encode_utf32_strict(name: &str, text: &str) -> Option<Vec<u8>> {
    let little_endian = match name {
        "utf_32" | "utf_32_le" => true,
        "utf_32_be" => false,
        _ => return None,
    };
    let mut encoded = Vec::with_capacity(text.len() * 4 + 4);
    if name == "utf_32" {
        encoded.extend_from_slice(&[0xff, 0xfe, 0x00, 0x00]);
    }
    for ch in text.chars() {
        let value = ch as u32;
        if little_endian {
            encoded.extend_from_slice(&value.to_le_bytes());
        } else {
            encoded.extend_from_slice(&value.to_be_bytes());
        }
    }
    Some(encoded)
}

pub(crate) fn decode_utf7_strict(payload: &[u8]) -> Option<String> {
    let mut decoded = String::with_capacity(payload.len());
    let mut index = 0usize;

    while index < payload.len() {
        let byte = payload[index];
        if byte > 0x7f {
            return None;
        }

        if byte != b'+' {
            decoded.push(byte as char);
            index += 1;
            continue;
        }

        if payload.get(index + 1) == Some(&b'-') {
            decoded.push('+');
            index += 2;
            continue;
        }

        let mut cursor = index + 1;
        let mut bits = 0u32;
        let mut bit_count = 0u8;
        let mut units = Vec::new();

        while let Some(&candidate) = payload.get(cursor) {
            let Some(value) = utf7_base64_value(candidate) else {
                break;
            };
            bits = (bits << 6) | value as u32;
            bit_count += 6;
            while bit_count >= 16 {
                bit_count -= 16;
                units.push(((bits >> bit_count) & 0xffff) as u16);
                bits &= (1 << bit_count) - 1;
            }
            cursor += 1;
        }

        if cursor == index + 1 || (bit_count > 0 && bits != 0) {
            return None;
        }

        let shifted = String::from_utf16(&units).ok()?;
        decoded.push_str(&shifted);

        if payload.get(cursor) == Some(&b'-') {
            cursor += 1;
        }
        index = cursor;
    }

    if decoded.starts_with('\u{feff}') {
        decoded.remove(0);
    }

    Some(decoded)
}

pub(crate) fn encode_utf7_strict(text: &str) -> Option<Vec<u8>> {
    let mut encoded = Vec::with_capacity(text.len());
    let mut shifted = Vec::new();

    for ch in text.chars() {
        if is_utf7_direct(ch) {
            flush_utf7_shifted(&mut shifted, &mut encoded)?;
            encoded.push(ch as u8);
        } else if ch == '+' {
            flush_utf7_shifted(&mut shifted, &mut encoded)?;
            encoded.extend_from_slice(b"+-");
        } else {
            let mut scratch = [0u16; 2];
            shifted.extend_from_slice(ch.encode_utf16(&mut scratch));
        }
    }

    flush_utf7_shifted(&mut shifted, &mut encoded)?;
    Some(encoded)
}

fn is_utf7_direct(ch: char) -> bool {
    matches!(
        ch,
        'A'..='Z'
            | 'a'..='z'
            | '0'..='9'
            | '\''
            | '('
            | ')'
            | ','
            | '-'
            | '.'
            | '/'
            | ':'
            | '?'
            | ' '
            | '\t'
            | '\r'
            | '\n'
    )
}

fn flush_utf7_shifted(shifted: &mut Vec<u16>, encoded: &mut Vec<u8>) -> Option<()> {
    if shifted.is_empty() {
        return Some(());
    }

    let mut bytes = Vec::with_capacity(shifted.len() * 2);
    for unit in shifted.drain(..) {
        bytes.extend_from_slice(&unit.to_be_bytes());
    }
    encoded.push(b'+');
    encode_base64_no_padding(&bytes, encoded);
    encoded.push(b'-');
    Some(())
}

fn encode_base64_no_padding(bytes: &[u8], encoded: &mut Vec<u8>) {
    const ALPHABET: &[u8; 64] = b"ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
    let mut index = 0usize;
    while index + 3 <= bytes.len() {
        let n = ((bytes[index] as u32) << 16)
            | ((bytes[index + 1] as u32) << 8)
            | bytes[index + 2] as u32;
        encoded.push(ALPHABET[((n >> 18) & 0x3f) as usize]);
        encoded.push(ALPHABET[((n >> 12) & 0x3f) as usize]);
        encoded.push(ALPHABET[((n >> 6) & 0x3f) as usize]);
        encoded.push(ALPHABET[(n & 0x3f) as usize]);
        index += 3;
    }

    match bytes.len() - index {
        1 => {
            let n = (bytes[index] as u32) << 16;
            encoded.push(ALPHABET[((n >> 18) & 0x3f) as usize]);
            encoded.push(ALPHABET[((n >> 12) & 0x3f) as usize]);
        }
        2 => {
            let n = ((bytes[index] as u32) << 16) | ((bytes[index + 1] as u32) << 8);
            encoded.push(ALPHABET[((n >> 18) & 0x3f) as usize]);
            encoded.push(ALPHABET[((n >> 12) & 0x3f) as usize]);
            encoded.push(ALPHABET[((n >> 6) & 0x3f) as usize]);
        }
        _ => {}
    }
}

fn utf7_base64_value(byte: u8) -> Option<u8> {
    match byte {
        b'A'..=b'Z' => Some(byte - b'A'),
        b'a'..=b'z' => Some(byte - b'a' + 26),
        b'0'..=b'9' => Some(byte - b'0' + 52),
        b'+' => Some(62),
        b'/' => Some(63),
        _ => None,
    }
}
